function [ce] = elastic_tensor (matprop)
%*************************************************************************************
%*       Elastic constitutive tensor Voigt notation                                 %*
%************************************************************************************* 


%*************************************************************************************
%
E=matprop(1);
nu=matprop(9);
A=E/((1+nu)*(1-2*nu));
B=1-nu;
C=1-2*nu;
%*************************************************************************************


%*************************************************************************************

        ce    = zeros(6,6);             % Init.
        ce(1,1)=B;
        ce(1,2)=nu;
        ce(1,3)=nu;
        ce(2,1)=nu;
        ce(2,2)=B;
        ce(2,3)=nu;
        ce(3,1)=nu;
        ce(3,2)=nu;
        ce(3,3)=B;
        ce(4,4)=C;
        ce(5,5)=C;
        ce(6,6)=C;
        ce = A*ce;
        

%*************************************************************************************

return